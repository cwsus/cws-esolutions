DELIMITER //

DROP TABLE IF EXISTS AUDIT.AUDIT_DATA //

CREATE TABLE AUDIT.AUDIT_DATA (
    SESSION_ID VARCHAR(128) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci NOT NULL,
    USERNAME VARCHAR(45) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci NOT NULL,
    CN VARCHAR(128) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci NOT NULL,
    USER_ROLE VARCHAR(45) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci NOT NULL,
    APPLICATION_ID VARCHAR(128) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci NOT NULL,
    APPLICATION_NAME VARCHAR(128) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci NOT NULL,
    REQUEST_TIMESTAMP TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP(),
    REQ_ACTION VARCHAR(45) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci NOT NULL,
    SOURCE_ADDRESS VARCHAR(45) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci NOT NULL,
    SOURCE_HOSTNAME VARCHAR(100) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci NOT NULL,
    INDEX IDX_AUDIT (SESSION_ID, USERNAME, CN, USER_ROLE, APPLICATION_NAME, REQ_ACTION, SOURCE_ADDRESS, SOURCE_HOSTNAME ASC),
    FULLTEXT KEY FT_AUDIT (SESSION_ID, USERNAME, CN, USER_ROLE, APPLICATION_NAME, REQ_ACTION, SOURCE_ADDRESS, SOURCE_HOSTNAME)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 ROW_FORMAT=COMPACT COLLATE utf8mb4_general_ci //
COMMIT //

GRANT SELECT, UPDATE, INSERT, DELETE, EXECUTE ON AUDIT.* TO 'appadm'@'localhost' //
GRANT SELECT, UPDATE, INSERT, DELETE, EXECUTE ON AUDIT.* TO 'appadm'@'appsrv.lan' //

