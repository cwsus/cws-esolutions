DROP PROCEDURE IF EXISTS AUDIT.getAuditInterval //
DROP PROCEDURE IF EXISTS AUDIT.insertAuditEntry //

CREATE PROCEDURE AUDIT.getAuditInterval(
    IN userguid VARCHAR(128) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci,
    IN startRow INT
)
BEGIN
    SELECT
        SESSION_ID,
        USERNAME,
        CN,
        USER_ROLE,
        APPLICATION_ID,
        APPLICATION_NAME,
        REQUEST_TIMESTAMP,
        REQ_ACTION,
        SOURCE_ADDRESS,
        SOURCE_HOSTNAME
    FROM AUDIT.AUDIT_DATA
    WHERE CN = userguid
    ORDER BY REQUEST_TIMESTAMP DESC
    LIMIT startRow, 20;
END //
COMMIT //

CREATE PROCEDURE AUDIT.insertAuditEntry(
    IN usersessid VARCHAR(128) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci,
    IN username VARCHAR(45) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci,
    IN userguid VARCHAR(128) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci,
    IN userrole VARCHAR(45) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci,
    IN applid VARCHAR(128) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci,
    IN applname VARCHAR(128) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci,
    IN useraction VARCHAR(45) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci,
    IN srcaddr VARCHAR(45) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci,
    IN srchost VARCHAR(128) CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci
)
BEGIN
    INSERT INTO AUDIT.AUDIT_DATA (SESSION_ID, USERNAME, CN, USER_ROLE, APPLICATION_ID, APPLICATION_NAME, REQUEST_TIMESTAMP, REQ_ACTION, SOURCE_ADDRESS, SOURCE_HOSTNAME)
    VALUES (usersessid, username, userguid, userrole, applid, applname, CURRENT_TIMESTAMP(), useraction, srcaddr, srchost);

    COMMIT;
END //
COMMIT //

GRANT EXECUTE ON PROCEDURE AUDIT.getAuditInterval TO 'appadm'@'localhost' //
GRANT EXECUTE ON PROCEDURE AUDIT.insertAuditEntry TO 'appadm'@'localhost' //

GRANT EXECUTE ON PROCEDURE AUDIT.getAuditInterval TO 'appadm'@'appsrv.lan' //
GRANT EXECUTE ON PROCEDURE AUDIT.insertAuditEntry TO 'appadm'@'appsrv.lan' //

DELIMITER ;
